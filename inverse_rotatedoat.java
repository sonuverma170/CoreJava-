public class inverse_rotatedoat {

        public static void main(String[] args) {
            int n=4,i,j;
            for(i=1;i<=n;i++)
            {
                for(j=1;j<=n-1;j++)
                {
                    System.out.print("");
                }
                for(j=i;j<=i;j++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }
            
        }

}
